/**************************************************************************/
/* Imports */
/**************************************************************************/

const express = require('express');


/**************************************************************************/
/* Configuration */
/**************************************************************************/

const app = express();

app.use('/', express.static(`${__dirname}/public`));


/**************************************************************************/
/* Initialization */
/**************************************************************************/

const port = process.env.PORT || 8888;
app.listen(port, () => `Listening on port ${port}`);